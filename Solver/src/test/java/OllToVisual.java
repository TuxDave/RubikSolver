public class OllToVisual {
        public static void main(String[] args) {
                String str = "100010100001000110111";
                System.out.println(" " + Character.toString(str.charAt(17)) + Character.toString(str.charAt(16))
                                + Character.toString(str.charAt(15)));
                System.out.println(Character.toString(str.charAt(18)) + Character.toString(str.charAt(1))
                                + Character.toString(str.charAt(2)) + Character.toString(str.charAt(3))
                                + Character.toString(str.charAt(14)));
                System.out.println(Character.toString(str.charAt(19)) + Character.toString(str.charAt(8))
                                + Character.toString(str.charAt(0)) + Character.toString(str.charAt(4))
                                + Character.toString(str.charAt(13)));
                System.out.println(Character.toString(str.charAt(20)) + Character.toString(str.charAt(7))
                                + Character.toString(str.charAt(6)) + Character.toString(str.charAt(5))
                                + Character.toString(str.charAt(12)));
                System.out.println(" " + Character.toString(str.charAt(9)) + Character.toString(str.charAt(10))
                                + Character.toString(str.charAt(11)));
        }
}
